package academy.learnprogramming;

public class Sauce {

    private int redSaucePrice;
    private int chiliSaucePrice;

    public Sauce(int redSaucePrice, int chiliSaucePrice) {
        this.redSaucePrice = redSaucePrice;
        this.chiliSaucePrice = chiliSaucePrice;
    }

    public int getRedSaucePrice() {
        return redSaucePrice;
    }

    public int getChiliSaucePrice() {
        return chiliSaucePrice;
    }
}
