package academy.learnprogramming;

public class AdditionalItems {

    private int softDrinkPrice;
    private int chipsPrice;

    public AdditionalItems(int softDrinkPrice, int chipsPrice) {
        this.softDrinkPrice = softDrinkPrice;
        this.chipsPrice = chipsPrice;
    }

    public int getSoftDrinkPrice() {
        return softDrinkPrice;
    }

    public int getChipsPrice() {
        return chipsPrice;
    }
}
